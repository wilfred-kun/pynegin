class Container:
    def __init__(self, x, y, width, height):
        self.xPos = x
        self.yPos = y
        self.width = width
        self.height = height

    def getXPos(self):
        return self.xPos

    def getYPos(self):
        return self.yPos

    def getWidth(self):
        return self.width

    def getHeight(self):
        return self.height
